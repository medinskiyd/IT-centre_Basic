	public void outAllWorkersSortSalary() {

		// ??????? ???????? ??????? ??? ??????????.
		ArrayList <Worker> WorkersWageRates =  new ArrayList <Worker>();
		WorkersWageRates.addAll(workers);
		WorkersWageRates.addAll(managers);
		WorkersWageRates.addAll(sellers);

		// ????????? ???????? ???????????.
		for (int i = 0; i < workers.size()+managers.size()+sellers.size(); i++){

		double min = WorkersWageRates.get(i).getWageRate();
		int imin = i;
			for(int j = i+1; j < workers.size()+managers.size()+sellers.size(); j++) {
				if (WorkersWageRates.get(j).getWageRate() < min) {
					min = WorkersWageRates.get(j).getWageRate();
					imin = j;
				}
			}
			// ?????????? ? ???????? ??????????? ?? ??????? ??????????? ????????.
			if (i!=imin) {
				WorkersWageRates.add(WorkersWageRates.get(i));
				WorkersWageRates.set(i, WorkersWageRates.get(imin));
				WorkersWageRates.set(imin, WorkersWageRates.get(workers.size()+managers.size()+sellers.size()));
				WorkersWageRates.remove(workers.size()+managers.size()+sellers.size());
			}
		}
		//??????? ?????????????? ???????? ???????.
		System.out.println("?????????? ?? ??????????? ????????: ");
		for (int i = 0; i < workers.size()+managers.size()+sellers.size(); i++){
			System.out.println(WorkersWageRates.get(i).getSurname()+" "+ WorkersWageRates.get(i).getName());
		}


	}

