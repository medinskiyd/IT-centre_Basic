package Основы.lesson1;

/* Проинициализируйте 3 переменные (int, byte, boolean) и выведите их в консоль (на экран). */

public class VarEx {

    public static void main(String[] args) {
        int firstVar;
        firstVar = 24;

        byte byteValue = 127;

        boolean boolVar = true;

        System.out.println("int - " + firstVar);
        System.out.println("byte - " + byteValue);
        System.out.println("boolean - " + boolVar);

    }
}