	public static String changeFirstSymbInWord (String lineFromMain) {
		
		final char spase = ' ';
		String BIGlineFromMain = lineFromMain.toUpperCase();
		String a = "";
		
		a = a + BIGlineFromMain.charAt(0);
		
		for (int i = 1; i < lineFromMain.length(); i++){
				
			 if (lineFromMain.charAt(i) == spase) {
				
				a = a + " " + BIGlineFromMain.charAt(i+1);
				
			} else  if ((lineFromMain.charAt(i) != spase) && (lineFromMain.charAt(i-1) != spase)) {
				
					a = a + lineFromMain.charAt(i) ; 
					}	
		}
		return a;	
	}
